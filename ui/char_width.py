@lru_cache(maxsize=4096)
def char_width(ch):
    """Return the visual width of a character in VRChat's chatbox.

    Uses actual advance widths from VRChat's NotoSans-Regular SDF font.
    Falls back to Unicode category heuristics for characters not in the font.
    """
    cp = ord(ch)
    if cp in _VRC_WIDTHS:
        return _VRC_WIDTHS[cp]

    cat = unicodedata.category(ch)
    if cat.startswith('M') or cat == 'Cf':
        return 0.0

    eaw = unicodedata.east_asian_width(ch)
    if eaw in ('F', 'W'):
        return 1.9
    if eaw == 'A':
        return 1.8

    if 0x2500 <= cp <= 0x259F:
        return 1.8
    if 0x2600 <= cp <= 0x26FF:
        return 1.8
    if 0x2700 <= cp <= 0x27BF:
        return 1.9
    if cp >= 0x1F000:
        return 2.0
    if 0x0B80 <= cp <= 0x0BFF or 0x0E00 <= cp <= 0x0E7F:
        return 1.8

    return 1.0


def count_visual_lines(text):
    """Count how many visual lines text occupies in VRChat's chatbox."""
    if not text:
        return 1
    total = 0
    for line in text.split(chr(10)):
        if len(line) == 0:
            total += 1
        else:
            total += math.ceil(sum(char_width(ch) for ch in line) / LINE_WIDTH_BUDGET)
    return total
